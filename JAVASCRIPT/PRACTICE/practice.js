//Primitive Data Types

//Number Data Types
let age = 24;
console.log(age);

//String Data Types
let fullName = "Anish";
console.log(fullName);

//Boolean Data Types
isFollow = true;
console.log(isFollow);

isName = false;
console.log(isName);

//Undefined Data Types
let a;
console.log(a);

//Null Data Types
let b = null;
console.log(b);

//BigInt Data Types
let x = BigInt("123");
console.log(x);

//Symbol Data Types
let y = Symbol("Hello!");
console.log(y);
